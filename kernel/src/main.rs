#![no_std]
#![no_main]

mod font;
mod vga;

use core::arch::asm;
use vga::Vga;

#[no_mangle]
extern "C" fn _start() -> ! {
    let vga = Vga::new();
    vga.write("Hello, world!", 0, 0);
    hcf();
}

#[panic_handler]
fn rust_panic(_info: &core::panic::PanicInfo) -> ! {
    hcf();
}

fn hcf() -> ! {
    unsafe {
        asm!("cli");
        loop {
            asm!("hlt");
        }
    }
}
